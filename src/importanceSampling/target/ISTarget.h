#ifndef KERNELO_ISTARGET_H
#define KERNELO_ISTARGET_H

#include <memory>
#include <utility>
#include "../../dataGeneration/StatModel.h"

namespace importanceSampling{
    /**
     * @struct ISTarget
     * The target law is used during the importance sampling algorithm. It provides the function that computes the logarithm
     * of its density which depends of the stat model used in the data generation module.
     */
    struct ISTarget{
    private:
        std::shared_ptr<DataGeneration::StatModel> target;
    public:
        /**
         * This method computes the log density of the target low, it should be overridden by the classes that inherit from
         * this class.
         * @param x : A sample generated by the proposition law
         * @param y : The observation
         * @param y_cov : The measure error of the observation
         * @return the value of the logarithm of the density
         */
        virtual double target_log_density(const vec &x, const vec &y, const vec &y_cov){
            return target->density_X_Y(x, y, y_cov);
        }

        void setTarget(std::shared_ptr<DataGeneration::StatModel> targetDistribution){
            target = std::move(targetDistribution);
        };
    };
}

#endif //KERNELO_ISTARGET_H
